          <h3></h3>
          Perdana Menteri Pakistan, Imran Khan, mendapatkan kecaman keras oleh
          masyarakat terkait dengan pernyataannya mengenai peningkatan kasus
          pemerkosaan di Pakistan. Ia menyalahkan meningkatnya kasus pemerkosaan
          di Pakistan karena pakaian yang dipakai korban. <br /><br />

          Khan mengakui bahwa insiden pemerkosaan terhadap wanita meningkat
          sangat cepat di Pakistan. Dia menyebut ini adalah konsekuensi dalam
          masyarakat manapun, di mana "vulgaritas sedang meningkat". Ia juga
          mengatakan bahwa "insiden pemerkosaan sebenarnya telah meningkat
          sangat pesat di masyarakat,". Dia menyarankan perempuan untuk menutupi
          tubuh mereka untuk mencegah timbulnya godaan.
          <br /><br />Pakistan adalah sebuah negara yang sangat konservatif
          dimana korban pelecehan seksual sering dicurigai atas tindak kriminal
          yang sangat jarang diselidiki secara serius. Sebagian besar kehidupan
          masyarakatnya dikunjungi aturan "kehormatan" yang di mana perempuan
          yang dianggap menimbulkan "rasa malu" pada keluarga, dan kerap
          dijadikan sasaran kekerasan atau pembunuhan. Dilansir dari AFP (Agence
          France-Presse), Pakistan hampir selalu menempati peringkat terburuk di
          dunia untuk masalah kesetaraan gender.<br /><br />

          Budaya pemerkosaan bukanlah mitos. Itu nyata dan itu berbahaya. Tidak
          dapat dipungkiri terjadinya peningkatan kasus pemerkosaan di Pakistan
          ini menggemparkan masyarakat terutama setelah mendengar tentang
          bagaimana tanggapan dari Perdana Menteri Pakistan, Imran Khan, dimana
          ia menanggapi bahwa pemerkosaan terjadi karena pakaian yang dipakai
          korban sehingga menunjukkan lekuk tubuh atau kulit mereka.
          <br /><br />
          Tentunya hal ini adalah hal yang salah karena ini menunjukkan budaya
          pemerkosaan secara tidak langsung berlaku di Pakistan. Budaya
          pemerkosaan adalah budaya dimana kekerasan seksual diperlakukan
          sebagai norma dan para korban disalahkan atas serangan mereka sendiri.
          Ini bukan hanya tentang kekerasan seksual itu sendiri, melainkan juga
          tentang norma dan institusi budaya yang melindungi pemerkosa,
          mempromosikan impunitas, mempermalukan korban, serta menuntut agar
          perempuan melakukan pengorbanan yang tidak masuk akal untuk
          menghindari kekerasan seksual. Budaya pemerkosaan menekan perempuan
          untuk mengorbankan kebebasan dan kesempatan mereka agar tetap aman,
          karena itu menempatkan beban keselamatan di pundak perempuan, dan
          menyalahkan mereka ketika mereka tidak berhasil.

