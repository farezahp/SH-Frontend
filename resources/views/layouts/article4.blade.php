          Selama ini, kita selalu melihat iklan entah di televisi atau media
          sosial yang menggambarkan bahwa seorang perempuan idealnya memiliki
          tubuh yang langsing, dilengkapi dengan alis yang tebal dan bibir merah
          merona, serta tubuh mulus tanpa bulu. Akibatnya, perempuan yang
          terlihat unik dianggap tidak memenuhi standar ideal sehingga mereka
          dicap sebagai ‘tidak cantik’ oleh beberapa budaya dan masyarakat
          sekitar kita. Hal inilah yang menyebabkan perempuan sering kali
          mendapatkan perlakuan diskriminasi di masyarakat.
          <br /><br />

          Maka dari itu, tidak heran lho Sisters, kalau banyak perempuan yang
          mengeluarkan uang lebih untuk tato alis, sulam alis, filler bibir,
          atau operasi plastik untuk memperindah penampilan.

          <br />
          <p>
            Ingat ya, kita tidak boleh asal menghakimi keputusan orang untuk
            mempercantik dirinya!
          </p>
          <br />Namun, lain halnya dengan perempuan yang mengenakan tato.
          Keputusan seorang perempuan memakai tato menimbulkan berbagai reaksi
          di masyarakat, baik itu positif maupun negatif. Padahal, setiap orang
          memiliki hak untuk mengekspresikan dirinya melalui ukiran tato yang
          ada di tubuhnya terlepas dari gender yang menghalangi mereka.

          <br /><br />

          Dalam Kamus Besar Bahasa Indonesia, tato adalah gambar (lukisan) pada
          kulit tubuh, sedangkan menato adalah melukis pada kulit tubuh dengan
          cara menusuki kulit dengan jarum halus kemudian memasukkan zat warna
          ke dalam bekas tusukan itu. Tato dianggap sebagai modifikasi yang
          tidak normal dan sehat karena prosesnya yang tidak alamiah dan cukup
          menyakitkan. Oleh karena itu, mudah menjadi sorotan masyarakat ketika
          ada seorang perempuan yang mengenakan tato di tubuhnya.
          <br /><br />
          Perempuan bertato mengalami stigmatisasi lebih berat daripada pria.
          Karena bagi pria, tato memperkuat gagasan maskulinitas tradisional,
          sementara bagi perempuan adalah cara menentang dan mereproduksi
          norma-norma yang dianggap sah atas diri mereka, (Kang & Jones, 2007).
          Hal ini membuktikan bahwa ada hubungan antara perempuan dengan tato
          yang dimilikinya, biasanya berisi makna khusus bagi pemiliknya.
          <br /><br />Terlepas dari itu, ada beberapa budaya asli dari Indonesia
          yang menggunakan tato sebagai ‘tanda’ pada perempuan.. Contohnya bagi
          perempuan suku Belu di Pulau Timor, tato merupakan simbol kecantikan
          sebagai medium daya tarik lawan. Lalu, pada masyarakat Sumba,
          perempuan merajah pergelangan kaki mereka dengan warna hitam pekat
          untuk menandakan bahwa mereka telah memiliki pasangan tetap (Dewi,
          2013:7) dan pada Suku Dayak Kayan, perempuan bertato lebih
          diperhitungkan derajatnya dibanding perempuan yang tidak bertato.
          <br /><br />Hal ini dapat disimpulkan bahwa, jangan selalu melihat
          perempuan hanya dari sampulnya saja. Bisa saja ada alasan tertentu
          mengapa seorang perempuan mengenakan tato. Entah karena faktor
          pengalaman perempuan yang tidak dapat diekspresikan dengan cara lain,
          ataupun adanya nilai-nilai budaya yang dirasa paling tepat untuk
          menunjukan identitas nya ketika mengenakan tato.

